class opPost:
    def post(self, date):
        char = date.split(" ")
        sep = ' " '
        lista_nueva = sep.join(char)
        print(lista_nueva)


class opPre:
    def pre(self, d):
        character = d.split(" ")
        for A in character:
            print(A)


class opInf:
    def inf(self, data):
        charI = data.split(" ")
        for A in charI:
            print(data)

